//! #problem-1 ------------------------------------------------------------------------------------------------------>
//* Write a program using "typeof" to check the type of a variable. For example, declare a variable "price" with a value of 555, now check the type in the console.

const price = 555;
console.log(typeof price); // output: number;

//! #problem-2 ------------------------------------------------------------------------------------------------------>
//* Declare a variable "learning" with the value true, and output it to the console. Then use "typeof" to see what type of variable it is.

const learning = true;
console.log(learning); // output: true;
console.log(typeof learning); // output: boolean;

